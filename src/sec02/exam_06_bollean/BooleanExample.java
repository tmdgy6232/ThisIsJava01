package sec02.exam_06_bollean;

public class BooleanExample {

    public static void main(String[] args) {

        /**
         *  boolean Type
         *
         * 조건, 반복문에서 자주 사용하는 boolean타입이다.
         *
         * 조건 분기, 메서드 분기 등 조건에 따라 다른 메서드를 사용해야 할 때 많이 사용되는 타입이다.
         * */
        boolean stop = true;

        if (stop){
            System.out.println(stop);
        } else {
            System.out.println(stop);
        }

    }
}
